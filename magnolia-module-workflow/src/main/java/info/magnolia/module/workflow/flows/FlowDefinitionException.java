package info.magnolia.module.workflow.flows;

public class FlowDefinitionException extends Exception {

    public FlowDefinitionException() {
        super();
    }

    public FlowDefinitionException(String msg, Throwable cause) {
        super(msg, cause);
    }

    public FlowDefinitionException(String msg) {
        super(msg);
    }

    public FlowDefinitionException(Throwable cause) {
        super(cause);
    }
}
